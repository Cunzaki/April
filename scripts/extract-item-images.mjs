#!/usr/bin/env node
/**
 * Parse dump Items module → src/game/item_images.lua + assets/manifest.json
 * Run: node scripts/extract-item-images.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const DUMP_CANDIDATES = [
  path.join(ROOT, "dump/scripts/ReplicatedStorage.Modules.Items.ModuleScript.lua"),
  path.join(ROOT, "dump/scripts/ModuleScript/ReplicatedStorage.Modules.Items.ModuleScript.lua"),
];
const DUMP = DUMP_CANDIDATES.find((p) => fs.existsSync(p));
const OUT_LUA = path.join(ROOT, "src/game/item_images.lua");
const MANIFEST = path.join(ROOT, "assets/manifest.json");

const TUNG_ID = "139818999438291";

function rbxId(str) {
  if (!str) return null;
  const m = String(str).match(/(\d{5,})/);
  return m ? m[1] : null;
}

function parseItems(text) {
  const byName = {};
  const assetIds = new Set();

  const nameRe = /Name\s*=\s*"([^"]+)"/g;
  let m;
  while ((m = nameRe.exec(text)) !== null) {
    const name = m[1];
    const start = m.index;
    const next = text.indexOf('Name = "', start + 8);
    const chunk = next > 0 ? text.slice(start, next) : text.slice(start, start + 8000);

    const imageStr = chunk.match(/Image\s*=\s*"(rbxassetid:[^"]+)"/);
    if (imageStr) {
      const id = rbxId(imageStr[1]);
      if (id) {
        byName[name] = { defaultId: id };
        assetIds.add(id);
        continue;
      }
    }

    const imageBlock = chunk.match(/Image\s*=\s*\{/);
    if (imageBlock) {
      const from = chunk.indexOf("Image = {") + "Image = {".length;
      let depth = 1;
      let i = from;
      while (i < chunk.length && depth > 0) {
        const c = chunk[i];
        if (c === "{") depth++;
        else if (c === "}") depth--;
        i++;
      }
      const inner = chunk.slice(from, i - 1);
      const variants = {};
      const pairRe = /(\w+|"(?:[^"\\]|\\.)*")\s*=\s*"(rbxassetid:[^"]+)"/g;
      let p;
      while ((p = pairRe.exec(inner)) !== null) {
        let key = p[1];
        if (key.startsWith('"')) key = key.slice(1, -1);
        const id = rbxId(p[2]);
        if (id) {
          variants[key] = id;
          assetIds.add(id);
        }
      }
      const def = variants.Default || variants.default;
      if (def || Object.keys(variants).length > 0) {
        byName[name] = { defaultId: def || Object.values(variants)[0], variants };
      }
    }
  }

  return { byName, assetIds };
}

function luaEscape(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function emitLua(byName) {
  const lines = [
    "-- AUTO-GENERATED by scripts/extract-item-images.mjs — do not edit by hand",
    "-- Source: dump/scripts/ReplicatedStorage.Modules.Items.ModuleScript.lua",
    "",
    "local M = {}",
    "",
    "M.by_name = {",
  ];

  const names = Object.keys(byName).sort();
  for (const name of names) {
    const entry = byName[name];
    lines.push(`    ["${luaEscape(name)}"] = { default = "${entry.default}"${entry.variants ? `, variants = ${JSON.stringify(entry.variants).replace(/"/g, '"')}` : ""} },`);
  }

  lines.push("}", "", "return M", "");
  return lines.join("\n");
}

function emitLuaProper(byName) {
  const lines = [
    "-- AUTO-GENERATED by scripts/extract-item-images.mjs — do not edit by hand",
    "-- Source: dump/scripts/ReplicatedStorage.Modules.Items.ModuleScript.lua",
    "",
    "local M = {}",
    "",
    "M.by_name = {",
  ];

  for (const name of Object.keys(byName).sort()) {
    const entry = byName[name];
    lines.push(`    ["${luaEscape(name)}"] = { default = "${entry.default}" },`);
  }

  lines.push("}", "", "function M.get_asset_id(name, variant)", "    local row = name and M.by_name[name]", "    if not row then return nil end", "    if variant and row.variants and row.variants[variant] then", "        return row.variants[variant]", "    end", "    return row.default", "end", "", "return M", "");
  return lines.join("\n");
}

function emitLuaWithVariants(byName) {
  const lines = [
    "-- AUTO-GENERATED by scripts/extract-item-images.mjs — do not edit by hand",
    "-- Source: dump/scripts/ReplicatedStorage.Modules.Items.ModuleScript.lua",
    "",
    "local M = {}",
    "",
    "M.by_name = {",
  ];

  for (const name of Object.keys(byName).sort()) {
    const entry = byName[name];
    let chunk = `    ["${luaEscape(name)}"] = { default = "${entry.defaultId}"`;
    if (entry.variants && Object.keys(entry.variants).length > 0) {
      chunk += ", variants = {";
      for (const [k, id] of Object.entries(entry.variants)) {
        chunk += ` ["${luaEscape(k)}"] = "${id}",`;
      }
      chunk += " }";
    }
    chunk += " },";
    lines.push(chunk);
  }

  lines.push(
    "}",
    "",
    "function M.get_asset_id(name, variant)",
    "    local row = name and M.by_name[name]",
    "    if not row then return nil end",
    "    if variant and row.variants and row.variants[variant] then",
    "        return row.variants[variant]",
    "    end",
    "    return row.default",
    "end",
    "",
    "return M",
    "",
  );
  return lines.join("\n");
}

if (!DUMP) {
  console.error("Missing Items module dump. Expected one of:");
  for (const p of DUMP_CANDIDATES) console.error("  ", p);
  process.exit(1);
}

const text = fs.readFileSync(DUMP, "utf8");
const { byName, assetIds } = parseItems(text);
assetIds.add(TUNG_ID);

fs.mkdirSync(path.dirname(OUT_LUA), { recursive: true });
fs.mkdirSync(path.dirname(MANIFEST), { recursive: true });

fs.writeFileSync(OUT_LUA, emitLuaWithVariants(byName));
fs.writeFileSync(
  MANIFEST,
  JSON.stringify(
    {
      generated: new Date().toISOString(),
      source: DUMP,
      tung: TUNG_ID,
      itemCount: Object.keys(byName).length,
      assetCount: assetIds.size,
      assets: [...assetIds].sort((a, b) => (BigInt(a) < BigInt(b) ? -1 : BigInt(a) > BigInt(b) ? 1 : 0)),
    },
    null,
    2,
  ),
);

console.log(
  `item_images.lua: ${Object.keys(byName).length} items, ${assetIds.size} unique asset IDs (+ tung)`,
);
