#!/usr/bin/env node
/**
 * Parse dump Items module → item_images.lua + item_catalog.lua
 * Run: node scripts/extract-item-catalog.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const DUMP = path.join(ROOT, "dump/scripts/ReplicatedStorage.Modules.Items.ModuleScript.lua");
const OUT_IMAGES = path.join(ROOT, "src/game/item_images.lua");
const OUT_CATALOG = path.join(ROOT, "src/game/item_catalog.lua");
const MANIFEST = path.join(ROOT, "assets/manifest.json");

const TUNG_ID = "139818999438291";

function rbxId(str) {
  if (!str) return null;
  const m = String(str).match(/(\d{5,})/);
  return m ? m[1] : null;
}

function luaEscape(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function parseItems(text) {
  const entries = [];
  const re = /\{\s*\n\s*Name\s*=\s*"([^"]+)"/g;
  let m;
  while ((m = re.exec(text))) {
    entries.push({ id: entries.length + 1, name: m[1], start: m.index });
  }

  const images = {};
  const catalog = [];
  const byAttribute = {};

  for (let i = 0; i < entries.length; i++) {
    const entry = entries[i];
    const nextStart = i + 1 < entries.length ? entries[i + 1].start : text.length;
    const chunk = text.slice(entry.start, Math.min(entry.start + 12000, nextStart));

    const type = chunk.match(/Type\s*=\s*"([^"]+)"/)?.[1] || "Unknown";
    const armorType = chunk.match(/ArmorType\s*=\s*"([^"]+)"/)?.[1] || null;
    const attr = chunk.match(/Attribute\s*=\s*"([^"]+)"/)?.[1] || null;

    if (attr) byAttribute[attr] = entry.name;

    const imageStr = chunk.match(/Image\s*=\s*"(rbxassetid:[^"]+)"/);
    if (imageStr) {
      const id = rbxId(imageStr[1]);
      if (id) images[entry.name] = { defaultId: id, variants: null };
    } else {
      const imageBlock = chunk.match(/Image\s*=\s*\{/);
      if (imageBlock) {
        const from = chunk.indexOf("Image = {") + "Image = {".length;
        let depth = 1;
        let j = from;
        while (j < chunk.length && depth > 0) {
          const c = chunk[j];
          if (c === "{") depth++;
          else if (c === "}") depth--;
          j++;
        }
        const inner = chunk.slice(from, j - 1);
        const variants = {};
        const pairRe = /(\w+|"(?:[^"\\]|\\.)*")\s*=\s*"(rbxassetid:[^"]+)"/g;
        let p;
        while ((p = pairRe.exec(inner)) !== null) {
          let key = p[1];
          if (key.startsWith('"')) key = key.slice(1, -1);
          const id = rbxId(p[2]);
          if (id) variants[key] = id;
        }
        const def = variants.Default || variants.default || Object.values(variants)[0];
        if (def) images[entry.name] = { defaultId: def, variants: Object.keys(variants).length ? variants : null };
      }
    }

    catalog.push({
      id: entry.id,
      name: entry.name,
      type,
      armorType,
      attribute: attr,
    });
  }

  return { images, catalog, byAttribute };
}

function emitImages(byName) {
  const lines = [
    "-- AUTO-GENERATED by scripts/extract-item-catalog.mjs — do not edit by hand",
    "-- Source: dump/scripts/ReplicatedStorage.Modules.Items.ModuleScript.lua",
    "",
    "local M = {}",
    "",
    "M.by_name = {",
  ];

  for (const name of Object.keys(byName).sort()) {
    const entry = byName[name];
    let chunk = `    ["${luaEscape(name)}"] = { default = "${entry.defaultId}"`;
    if (entry.variants && Object.keys(entry.variants).length > 0) {
      chunk += ", variants = {";
      for (const [k, id] of Object.entries(entry.variants)) {
        chunk += ` ["${luaEscape(k)}"] = "${id}",`;
      }
      chunk += " }";
    }
    chunk += " },";
    lines.push(chunk);
  }

  lines.push(
    "}",
    "",
    "function M.get_asset_id(name, variant)",
    "    local row = name and M.by_name[name]",
    "    if not row then return nil end",
    "    if variant and row.variants and row.variants[variant] then",
    "        return row.variants[variant]",
    "    end",
    "    return row.default",
    "end",
    "",
    "return M",
    "",
  );
  return lines.join("\n");
}

function emitCatalog(catalog, byAttribute) {
  const lines = [
    "-- AUTO-GENERATED by scripts/extract-item-catalog.mjs — do not edit by hand",
    `-- Items: ${catalog.length} entries from dump`,
    "",
    "local M = {}",
    "",
    "M.by_id = {",
  ];

  for (const row of catalog) {
    let chunk = `    [${row.id}] = { name = "${luaEscape(row.name)}", type = "${luaEscape(row.type)}"`;
    if (row.armorType) chunk += `, armor_type = "${luaEscape(row.armorType)}"`;
    if (row.attribute) chunk += `, attribute = "${luaEscape(row.attribute)}"`;
    chunk += " },";
    lines.push(chunk);
  }

  lines.push("}", "", "M.by_attribute = {");
  for (const [attr, name] of Object.entries(byAttribute).sort((a, b) => a[0].localeCompare(b[0]))) {
    lines.push(`    ["${luaEscape(attr)}"] = "${luaEscape(name)}",`);
  }
  lines.push("}", "", "M.by_name = {");
  for (const row of catalog) {
    let chunk = `    ["${luaEscape(row.name)}"] = { id = ${row.id}, type = "${luaEscape(row.type)}"`;
    if (row.armorType) chunk += `, armor_type = "${luaEscape(row.armorType)}"`;
    if (row.attribute) chunk += `, attribute = "${luaEscape(row.attribute)}"`;
    chunk += " },";
    lines.push(chunk);
  }
  lines.push(
    "}",
    "",
    "function M.get_by_name(name)",
    "    return name and M.by_name[name] or nil",
    "end",
    "",
    "function M.get(id)",
    "    return id and M.by_id[id] or nil",
    "end",
    "",
    "function M.get_by_attribute(attr)",
    "    local name = attr and M.by_attribute[attr]",
    "    if not name then return nil end",
    "    return { name = name }",
    "end",
    "",
    "function M.name_for_armor_model(model_name)",
    "    if not model_name or model_name:sub(1, 6) ~= \"Armor_\" then return nil end",
    "    local id, skin = model_name:match(\"^Armor_(%d+)/(.+)$\")",
    "    if not id then",
    "        id = model_name:match(\"^Armor_(%d+)$\")",
    "        skin = nil",
    "    end",
    "    if id then",
    "        local row = M.by_id[tonumber(id)]",
    "        if row then return row.name, skin end",
    "    end",
    "    local key = model_name:match(\"^(.-)/\") or model_name",
    "    key = key:gsub(\" \", \"_\")",
    "    local num = key:match(\"^Armor_(%d+)$\")",
    "    if num then",
    "        local row = M.by_id[tonumber(num)]",
    "        if row then return row.name, model_name:match(\"^.-/(.+)$\") end",
    "    end",
    "    local attr = key:match(\"^Armor_(.+)$\")",
    "    if attr then",
    "        local row = M.get_by_attribute(attr)",
    "        if row then return row.name, model_name:match(\"^.-/(.+)$\") end",
    "    end",
    "    return nil",
    "end",
    "",
    "return M",
    "",
  );
  return lines.join("\n");
}

if (!fs.existsSync(DUMP)) {
  console.error("Missing dump:", DUMP);
  process.exit(1);
}

const text = fs.readFileSync(DUMP, "utf8");
const { images, catalog, byAttribute } = parseItems(text);
const assetIds = new Set([TUNG_ID]);
for (const entry of Object.values(images)) {
  assetIds.add(entry.defaultId);
  if (entry.variants) {
    for (const id of Object.values(entry.variants)) assetIds.add(id);
  }
}

fs.mkdirSync(path.dirname(OUT_IMAGES), { recursive: true });
fs.mkdirSync(path.dirname(MANIFEST), { recursive: true });

fs.writeFileSync(OUT_IMAGES, emitImages(images));
fs.writeFileSync(OUT_CATALOG, emitCatalog(catalog, byAttribute));
fs.writeFileSync(
  MANIFEST,
  JSON.stringify(
    {
      generated: new Date().toISOString(),
      source: "dump/scripts/ReplicatedStorage.Modules.Items.ModuleScript.lua",
      tung: TUNG_ID,
      itemCount: catalog.length,
      imageCount: Object.keys(images).length,
      assetCount: assetIds.size,
      assets: [...assetIds].sort((a, b) => (BigInt(a) < BigInt(b) ? -1 : BigInt(a) > BigInt(b) ? 1 : 0)),
    },
    null,
    2,
  ),
);

console.log(
  `catalog: ${catalog.length} items, images: ${Object.keys(images).length}, assets: ${assetIds.size}`,
);
